import { mkdir, readdir, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const architectureDir = path.join(repoRoot, "docs", "architecture");
const snapshotPath = path.join(architectureDir, "current-structure.md");

const focusDirs = [
  "app",
  "components",
  "lib",
  "public",
  "docs/design",
  "docs/architecture",
  "docs/plan",
  "docs/superpowers",
  "scripts"
];
const ignoreNames = new Set(["node_modules", ".next", ".git", ".DS_Store"]);

async function readTree(rootDir, prefix = "") {
  const entries = await readdir(rootDir, { withFileTypes: true });
  const filtered = entries.filter((entry) => !ignoreNames.has(entry.name));
  filtered.sort((a, b) => a.name.localeCompare(b.name));

  const lines = [];
  for (const entry of filtered) {
    const fullPath = path.join(rootDir, entry.name);
    const isDir = entry.isDirectory();
    lines.push(`${prefix}${entry.name}${isDir ? "/" : ""}`);

    if (isDir) {
      const childLines = await readTree(fullPath, `${prefix}  `);
      lines.push(...childLines);
    }
  }

  return lines;
}

async function buildSnapshot() {
  const parts = [
    "# Current Repository Structure Snapshot",
    "",
    "This file is generated by `npm run sync:governance`.",
    "Refresh it whenever the repository structure changes.",
    ""
  ];

  for (const dir of focusDirs) {
    const fullPath = path.join(repoRoot, dir);
    try {
      const stats = await stat(fullPath);
      if (!stats.isDirectory()) {
        continue;
      }
    } catch {
      continue;
    }

    parts.push(`## ${dir}`);
    const treeLines = await readTree(fullPath, "");
    parts.push("```txt");
    parts.push(`${dir}/`);
    for (const line of treeLines) {
      parts.push(`  ${line}`);
    }
    parts.push("```");
    parts.push("");
  }

  return `${parts.join("\n").trimEnd()}\n`;
}

await mkdir(architectureDir, { recursive: true });
const snapshot = await buildSnapshot();
await writeFile(snapshotPath, snapshot, "utf8");

process.stdout.write(`Updated ${path.relative(repoRoot, snapshotPath)}\n`);
